    private void srediPanel() {
        napuniComboTipBaze();
    }

    private void napuniComboTipBaze() {
        jcmbDBTip.removeAllItems();
        jcmbDBTip.addItem("-- Izaberite tip baze podataka --");
        jcmbDBTip.addItem("Access");
        jcmbDBTip.addItem("MySQL");
    }

    private boolean izvrsiValidacijuUnetihParametara() {
        boolean validacija = true;
        if (jcmbDBTip.getSelectedIndex() == 0) {
            jlblerrDBTip.setText("Izabeire tip baze podataka");
            validacija = false;
        } else {
            jlblerrDBTip.setText("");
        }
        if (jtxtDBNaziv.getText().trim().isEmpty()) {
            jlblerrDBNaziv.setText("Unesite naziv");
            validacija = false;
        } else {
            jlblerrDBNaziv.setText("");
        }
        if (jtxtDBUsername.getText().trim().isEmpty()) {
            jlblerrDBUsername.setText("Unesite username");
            validacija = false;
        } else {
            jlblerrDBUsername.setText("");
        }
        if(!validacija){
            jlblDBStatus.setText("Pravilno popunite parametre");
        } else {
            jlblDBStatus.setText("");
        }
        return validacija;
    }

