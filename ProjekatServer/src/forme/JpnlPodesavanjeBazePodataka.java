    private void jbtnDBPoveziSeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDBPoveziSeActionPerformed
        // TODO add your handling code here:
        if (izvrsiValidacijuUnetihParametara()) {
            Konfiguracija.vratiInstancu().postaviNaziv(jtxtDBNaziv.getText().trim());
            if (jcmbDBTip.getSelectedIndex() == 1) {
                konfiguracija.Konfiguracija.vratiInstancu().postaviTipDB("access");
            }
            if (jcmbDBTip.getSelectedIndex() == 2) {
                konfiguracija.Konfiguracija.vratiInstancu().postaviTipDB("mysql");
            }
            
            DBKonekcija db = new DBKonekcija();
            try {
                db.otvoriKonekciju(Konfiguracija.vratiInstancu().vratiURL(), jtxtDBUsername.getText().trim(), jtxtDBPassword.getText().trim());
                db.zatvoriKonekciju();
                Konfiguracija.vratiInstancu().postaviUsername(jtxtDBUsername.getText().trim());
                Konfiguracija.vratiInstancu().postaviPassword(jtxtDBPassword.getText().trim());
                Konfiguracija.vratiInstancu().postaviPodesenostBaze("t");
                jlblDBStatus.setText("Uspesno povezivanje sa bazom podataka");
                forma.promeniPanel(1);
            } catch (ClassNotFoundException ex) {
                ex.printStackTrace();
                jlblDBStatus.setText("Neuspesno povezivanje. Problemi sa ucitavanjem drajvera");
            } catch (SQLException ex) {
                ex.printStackTrace();
                jlblDBStatus.setText("Neuspesno povezivanje");
            }

        }
    }//GEN-LAST:event_jbtnDBPoveziSeActionPerformed

