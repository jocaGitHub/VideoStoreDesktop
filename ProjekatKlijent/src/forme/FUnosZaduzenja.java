    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        Film f = (Film) jcomboFilmovi.getSelectedItem();
        Clan c = (Clan) jcomboClanovi.getSelectedItem();
        Kopija k = ((ModelTabeleKopija) jtblKopije.getModel()).vratiKopiju(0);

        Zaduzenje z = new Zaduzenje();
        z.setClan(c);
        z.setKopija(k);
        z.setZaduzenjeID(jtxtZaduzenjeID.getText());
        z.setRazduzio(null);
        z.setStatusZaduzenja("nije vraceno");
        z.setZaduzio((Radnici) sesija.Sesija.vratiInstancu().getMapa().get("radnik"));

        Date datum = null;
        String datumS = null;
        if (jtxtDatumZaduzenja.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "niste uneli datum razduzenja");
            return;
        } else {
            datumS = jtxtDatumZaduzenja.getText().trim();
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        try {
            datum = sdf.parse(datumS);
            z.setDatumZaduzenja(datum);

            boolean b = KontrolerKomunikacije.sacuvajZaduzenje(z, this);
            if (b == true) {
                listaZaduzenja = KontrolerKomunikacije.uzmiListuZaduzenjaZaClana(clan, this);
                jtblZaduzenja.setModel(new ModelTabeleZaduzenja(listaZaduzenja));
                jbtnSacuvaj.setVisible(false);
                jbtnDodajJos.setVisible(true);
                jbtnOtkazi.setVisible(false);
                jbtnOk.setVisible(false);
                otkazi = false;
                jPanelDodatno.setVisible(false);
                jbtnOtkaziIzmenu.setVisible(false);
                JOptionPane.showMessageDialog(this, "uspesno ste uneli zaduzenje");
            } else {
//                JOptionPane.showMessageDialog(this, "niste uspeli da sacuvate");
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti zaduzenje.\n Unesite datum u formatu dd/MM/yyyy");
        }


    }//GEN-LAST:event_jbtnSacuvajActionPerformed

