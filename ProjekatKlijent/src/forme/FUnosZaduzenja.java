    private void jcomboFilmoviItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcomboFilmoviItemStateChanged
        listaKopija = KontrolerKomunikacije.vratiListuKopija(this);
        listaZaduzenja = KontrolerKomunikacije.uzmiZaduzenja(this);
        List<Kopija> listaZaFilm = new ArrayList<>();
        List<Kopija> listaNevracenih = new ArrayList<>();
        if (listaKopija != null) {
            for (Kopija k : listaKopija) {
                if (k.getFilm().equals(jcomboFilmovi.getSelectedItem())) {
                    listaZaFilm.add(k);
                }
            }
            for (Zaduzenje z : listaZaduzenja) {
                for (Kopija k : listaZaFilm) {
                    if (z.getKopija().equals(k) && z.isStatusZaduzenja().equals("nije vraceno")) {
                        listaNevracenih.add(k);
                    }
                }
            }
            for (Kopija k : listaNevracenih) {
                listaZaFilm.remove(k);
            }
            jtblKopije.setModel(new ModelTabeleKopija(listaZaFilm));

        }
    }//GEN-LAST:event_jcomboFilmoviItemStateChanged

